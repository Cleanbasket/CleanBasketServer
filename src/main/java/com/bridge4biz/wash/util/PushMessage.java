package com.bridge4biz.wash.util;

public class PushMessage {
	public Integer constant = 0;
	public Integer uid = 0;
	public Integer oid = 0;

	public PushMessage() {

	}

	public PushMessage(Integer constant, Integer uid, Integer oid) {
		this.constant = constant;
		this.uid = uid;
		this.oid = oid;
	}
}
